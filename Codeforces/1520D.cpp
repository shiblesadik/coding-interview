#include <bits/stdc++.h>
using namespace std;

int test, n, l ,r, arr[200005] = {0}, low = 0, high = 0;
long long int m = 0, ans = 0;
map<int, int> mp;

int main (void) {
//  ios_base::sync_with_stdio(false);
//  cin.tie(NULL);
//  cout.tie(NULL);
  scanf("%d", &test);
  while (test--) {
    scanf("%d", &n);
    m = 0;
    ans = 0;
    mp.clear();
    for (int i = 1 ; i <= n; ++i) {
      scanf("%d", &arr[i]);
    }
    for (int i = 1; i <= n; ++i) {
      mp[arr[i] - i]++;
    }
    for (auto it: mp) {
      m = it.second;
      ans += (m * (m - 1)) >> 1;
    }
    printf("%lld\n", ans);
  }
  return 0;
}
